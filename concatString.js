//Faça uma função chamada concatString que recebe dois parâmetros chamados val1 e val2 do tipo string 
//e retorna uma string com a concatenação dos dois parâmetros separados por um espaço

function concatString(val1, val2)
{
    //concateno usando (+) as 2 variavel deixando um " " entre eles
    return val1 + ' ' + val2;
}